#include <REGX52.H>

void main() {
	/*
	按位左移： 0011 1100<<1          -> 0111 1000
	按位右移:  0011 1100>>2          -> 0000 1111
	按位与:    0001 1000 & 0010 1010 -> 0000 1000
	按位或:    0001 1000 | 0010 1010 -> 0011 1010
	按位异或:  0001 1000 ^ 0010 1010 -> 0011 0010
	按位取反: ~0001 1000             -> 1001 0111
	*/
	while (1) {
		// 	独立按键控制 LED 亮灭，同时按住 K1 和 K2 灯亮
		if (P3_1 == 0 && P3_0 == 0) {
			P2_0 = 0;
		} else {
			P2_0 = 1;
		}
	}
}